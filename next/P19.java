package com.next;

public class P19 {

	public static void main(String[] args) {

		int x = 10;

		long x1 = x;
		System.out.println(x1);

		float x2 = x;
		System.out.println(x2);

		double x3 = x;
		System.out.println(x3);

		byte x4 = (byte) x;
		System.out.println(x4);

		short x5 = (short) x;
		System.out.println(x5);
	}

}
